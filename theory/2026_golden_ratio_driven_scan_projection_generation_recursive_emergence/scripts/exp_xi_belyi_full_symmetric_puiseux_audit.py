@dataclass(frozen=True)
class BranchDiscRow:
    r: int
    y_r: str
    epsilon_r: int
    disc_formula_ok: bool
    derivative_formula_ok: bool
    local_y1_order_ok: bool
    local_yinfty_type_ok: bool
    local_yr_double_root_ok: bool
    cycle_sigma_1: str
    cycle_sigma_yr: str
    cycle_sigma_infty: str
    generated_group_order: int
    generated_group_is_full_symmetric: bool


@dataclass(frozen=True)
class ScalingRow:
    r: int
    n: int
    max_abs_error: str
    n_times_max_abs_error: str


def _poly_pi(r: int, lam: sp.Symbol, y: sp.Symbol) -> sp.Expr:
    return sp.expand((lam - 1) ** r + (y - 1) * lam ** (r - 1))


def _is_constant_in(expr: sp.Expr, sym: sp.Symbol) -> bool:
    expr = sp.together(sp.simplify(expr))
    num, den = expr.as_numer_denom()
    try:
        deg_num = sp.Poly(num, sym, domain=sp.QQ).degree()
        deg_den = sp.Poly(den, sym, domain=sp.QQ).degree()
    except Exception:
        return False
    return deg_num == 0 and deg_den == 0


def _cycle_type_str(perm: Permutation, degree: int) -> str:
    cyc_lengths = sorted((len(c) for c in perm.cyclic_form), reverse=True)
    moved = sum(cyc_lengths)
    fixed = degree - moved
    parts = [str(v) for v in cyc_lengths] + (["1"] * fixed)
    if not parts:
        parts = ["1"] * degree
    return "(" + ")(".join(parts) + ")"


def _powsum_newton(poly: sp.Poly, n: int) -> sp.Expr:
    # Monic polynomial x^d + a_1 x^(d-1) + ... + a_d
    coeffs = [sp.simplify(c) for c in poly.all_coeffs()]
    d = poly.degree()
    if coeffs[0] != 1:
        raise RuntimeError("Expected monic polynomial for Newton sums.")
    a = coeffs[1:]

    s: Dict[int, sp.Expr] = {0: sp.Integer(d)}
    for k in range(1, n + 1):
        if k <= d:
            rhs = -sum(a[j - 1] * s[k - j] for j in range(1, k))
            rhs -= k * a[k - 1]
            s[k] = sp.simplify(rhs)
        else:
            rhs = -sum(a[j - 1] * s[k - j] for j in range(1, d + 1))
            s[k] = sp.simplify(rhs)
    return sp.simplify(s[n])


def _complex_from_sympy(z: sp.Expr, dps: int = 80) -> mp.mpc:
    zr = sp.re(z).evalf(dps)
    zi = sp.im(z).evalf(dps)
    return mp.mpc(str(zr), str(zi))


