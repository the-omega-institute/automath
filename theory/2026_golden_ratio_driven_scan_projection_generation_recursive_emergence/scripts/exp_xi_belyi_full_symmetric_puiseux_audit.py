"""
Audit certificate for the explicit Belyi-type family

  Pi_r(lambda,y) = (lambda-1)^r + (y-1) lambda^(r-1),
  f_r(lambda) = 1 - (lambda-1)^r / lambda^(r-1).

Verified items:
  - closed discriminant factorization in y with sign epsilon_r in {+1,-1},
  - branch-value formula y_r = 1 + r^r/(r-1)^(r-1),
  - local ramification checks at y=1, y=y_r, y=infinity,
  - permutation certificate for full symmetric group generation,
  - r=3 recurrence/initial values/discriminant factorization,
  - numerical scaling-limit audit near y=1 at n^{-r} scale.

Outputs:
  - artifacts/export/xi_belyi_full_symmetric_puiseux_audit.json
  - sections/generated/eq_xi_belyi_family_discriminant_recurrence_r3.tex
  - sections/generated/tab_xi_belyi_branch_discriminant_group_audit_r2_r9.tex
  - sections/generated/tab_xi_belyi_scaling_limit_audit.tex
"""

