#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Audit certificate for the window-6 long-root Bloch cubic and bielliptic data.

This script verifies:
  - the chiral cubic characteristic polynomials,
  - the three spectral bands and their numerical edges,
  - the branch discriminant of the genus-2 hyperelliptic model,
  - the cubic discriminant of the first elliptic quotient,
  - the boundary/short/long gauge-volume split.

Outputs:
  - artifacts/export/window6_long_bloch_bielliptic_audit.json
  - sections/generated/eq_window6_long_bloch_band_edges.tex
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Dict, List

import sympy as sp

from common_paths import export_dir, generated_dir


def _factor_string(n: int) -> str:
    pieces: List[str] = []
    for p, e in sorted(sp.factorint(int(n)).items()):
        if e == 1:
            pieces.append(str(p))
        else:
            pieces.append(f"{p}^{e}")
    return " * ".join(pieces)


def _float_list(values: List[sp.Expr], digits: int = 10) -> List[str]:
    return [f"{float(sp.N(v, 50)):.{digits}f}" for v in values]


def _write_band_edges_tex(out_path: Path, a_vals: List[str], b_vals: List[str]) -> None:
    lines = [
        "% AUTO-GENERATED by scripts/exp_window6_long_bloch_bielliptic_audit.py",
        r"\[",
        rf"a_1\approx {a_vals[0]},\qquad b_1\approx {b_vals[0]},",
        r"\]",
        r"\[",
        rf"b_2\approx {b_vals[1]},\qquad a_2\approx {a_vals[1]},",
        r"\]",
        r"\[",
        rf"a_3\approx {a_vals[2]},\qquad b_3\approx {b_vals[2]}.",
        r"\]",
        "",
    ]
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    print("[start] window6_long_bloch_bielliptic_audit", flush=True)

    lam, z, u, x = sp.symbols("lam z u x")

    M = sp.diag(4, 2, 3)
    Q = sp.Matrix([[0, 0, 1], [1, 0, 0], [0, 1, 0]])

    H_plus = M + z * Q + z**-1 * (Q**2)
    H_minus = M - (z * Q + z**-1 * (Q**2))

    P = sp.expand((lam - 1) * (lam - 3) * (lam - 5))
    char_plus = sp.expand((lam * sp.eye(3) - H_plus).det())
    char_minus = sp.expand((lam * sp.eye(3) - H_minus).det())

    if sp.simplify(char_plus - (P - (z**3 + z**-3))) != 0:
        raise RuntimeError("Unexpected H_plus characteristic polynomial.")
    if sp.simplify(char_minus - (P + (z**3 + z**-3))) != 0:
        raise RuntimeError("Unexpected H_minus characteristic polynomial.")

    roots_p_plus_2 = sorted(sp.re(r) for r in sp.nroots(P + 2, n=50))
    roots_p_minus_2 = sorted(sp.re(r) for r in sp.nroots(P - 2, n=50))

    branch_poly = sp.expand(P**2 - 4)
    branch_disc = int(sp.discriminant(sp.Poly(branch_poly, lam), lam))

    shifted_branch_poly = sp.expand((((x + 2) * x * (x - 2)) ** 2) - 4)
    e_plus_cubic = u**3 - 8 * u**2 + 16 * u - 4
    e_plus_disc = int(sp.discriminant(sp.Poly(e_plus_cubic, u), u))

    log2 = sp.log(2)
    log3 = sp.log(3)

    g_boundary = sp.Rational(3, 64) * log2
    g_short = sp.Rational(16, 64) * log2 + sp.Rational(5, 64) * log3
    g_long = sp.Rational(20, 64) * log2 + sp.Rational(8, 64) * log3
    g_total = sp.simplify(g_boundary + g_short + g_long)

    k_boundary = sp.Rational(6, 64) * log2
    k_short = sp.Rational(42, 64) * log2
    k_long = sp.Rational(40, 64) * log2 + sp.Rational(12, 64) * log3
    k_total = sp.simplify(k_boundary + k_short + k_long)

    a_vals = _float_list(list(roots_p_plus_2))
    b_vals = _float_list(list(roots_p_minus_2))

    data: Dict[str, object] = {
        "charpoly_plus": sp.sstr(char_plus),
        "charpoly_minus": sp.sstr(char_minus),
        "band_edges": {
            "a": a_vals,
            "b": b_vals,
        },
        "branch_polynomial": sp.sstr(branch_poly),
        "branch_discriminant": {
            "integer": branch_disc,
            "factorization": _factor_string(branch_disc),
        },
        "shifted_branch_polynomial": sp.sstr(shifted_branch_poly),
        "eplus_discriminant": {
            "integer": e_plus_disc,
            "factorization": _factor_string(e_plus_disc),
        },
        "gauge_split": {
            "g_boundary": {
                "exact": "3*log(2)/64",
                "numeric": f"{float(sp.N(g_boundary, 50)):.6f}",
            },
            "g_short": {
                "exact": "(16*log(2) + 5*log(3))/64",
                "numeric": f"{float(sp.N(g_short, 50)):.6f}",
            },
            "g_long": {
                "exact": "(20*log(2) + 8*log(3))/64",
                "numeric": f"{float(sp.N(g_long, 50)):.6f}",
            },
            "g_total": {
                "exact": sp.sstr(g_total),
                "numeric": f"{float(sp.N(g_total, 50)):.6f}",
            },
            "k_boundary": {
                "exact": "6*log(2)/64",
                "numeric": f"{float(sp.N(k_boundary, 50)):.6f}",
            },
            "k_short": {
                "exact": "42*log(2)/64",
                "numeric": f"{float(sp.N(k_short, 50)):.6f}",
            },
            "k_long": {
                "exact": "(40*log(2) + 12*log(3))/64",
                "numeric": f"{float(sp.N(k_long, 50)):.6f}",
            },
            "k_total": {
                "exact": sp.sstr(k_total),
                "numeric": f"{float(sp.N(k_total, 50)):.6f}",
            },
        },
    }

    json_path = export_dir() / "window6_long_bloch_bielliptic_audit.json"
    tex_path = generated_dir() / "eq_window6_long_bloch_band_edges.tex"

    json_path.parent.mkdir(parents=True, exist_ok=True)
    json_path.write_text(json.dumps(data, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    _write_band_edges_tex(tex_path, a_vals, b_vals)

    print(f"[done] wrote {json_path}", flush=True)
    print(f"[done] wrote {tex_path}", flush=True)


if __name__ == "__main__":
    main()
