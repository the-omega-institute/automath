def _postprocess_strip_comment_timestamps() -> float:
    """Always run the timestamp-comment stripping pass.

    This is intentionally outside the step cache because upstream steps may be
    skipped by cache while manuscript sources could still contain (or regain)
    auto-inserted timestamp comments.
    """
    script_path = scripts_dir() / "util_strip_comment_timestamps.py"
    if not script_path.is_file():
        print(f"[run_all] WARN missing postprocess script: {script_path}", flush=True)
        return 0.0

    cmd = [sys.executable, str(script_path), "--root", str(paper_root())]
    print(f"[run_all] POST strip_comment_timestamps: {' '.join(cmd)}", flush=True)
    t0 = time.time()
    subprocess.check_call(cmd, cwd=str(paper_root()))
    return float(time.time() - t0)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Run reproducible experiment pipeline (with step cache)."
    )
    parser.add_argument(
        "--force", action="store_true", help="Force rerun all steps (ignore cache)."
    )
    parser.add_argument(
        "--no-cache", action="store_true", help="Disable cache (always run)."
    )
    args = parser.parse_args()

    steps = build_steps()
    if not steps:
        print("[run_all] No steps configured yet.", flush=True)
        return

    cache = _load_cache()
    steps_cache: Dict[str, object] = dict(cache.get("steps", {}))  # type: ignore[assignment]

    t0_all = time.time()
    step_times: Dict[str, float] = {}

    for st in steps:
        script_path = scripts_dir() / st.script
        if not script_path.is_file():
            raise SystemExit(f"Missing script: {script_path}")

        sig = _step_signature(script_path, st.args)
        ok, missing = _outputs_ok(st)
        cached = steps_cache.get(st.name)
        if ok and (not args.force) and (not args.no_cache) and cached == sig:
            print(f"[run_all] SKIP {st.name} (cache hit)", flush=True)
            continue

        # Cache warm-up: if outputs already exist but cache has no entry,
        # we adopt the current signature without rerunning (auditable, avoids slow cold-start).
        if ok and (not args.force) and (not args.no_cache) and cached is None:
            steps_cache[st.name] = sig
            cache["steps"] = steps_cache
            _write_cache(cache)
            print(
                f"[run_all] SKIP {st.name} (cache warm-up: outputs already present)",
                flush=True,
            )
            continue

        if ok and (not args.force) and (not args.no_cache) and cached != sig:
            # Outputs exist but signature changed; rerun for auditability.
            print(f"[run_all] RERUN {st.name} (signature changed)", flush=True)
        elif not ok:
            print(
                f"[run_all] RERUN {st.name} (missing outputs: {', '.join(missing)})",
                flush=True,
            )

        cmd = [sys.executable, str(script_path), *list(st.args)]
        print(f"[run_all] RUN {st.name}: {' '.join(cmd)}", flush=True)
        t0 = time.time()
        subprocess.check_call(cmd, cwd=str(paper_root()))
        dt = time.time() - t0

        for rel in st.expected_outputs:
            p = paper_root() / rel
            if not _nonempty(p):
                raise SystemExit(f"[run_all] expected output missing/empty: {p}")
        step_times[st.name] = float(dt)
        print(f"[run_all] OK {st.name} elapsed_s={dt:.3f}", flush=True)

        # Update cache after a successful step.
        steps_cache[st.name] = sig
        cache["steps"] = steps_cache
        _write_cache(cache)

    dt_post = _postprocess_strip_comment_timestamps()
    step_times["post_strip_comment_timestamps"] = float(dt_post)

    dt_all = time.time() - t0_all
    print(f"[run_all] ALL DONE elapsed_s={dt_all:.3f}", flush=True)

    # Write a small timing audit (useful for performance regressions).
    timing_path = paper_root() / "artifacts" / "export" / "run_all_timing.json"
    timing_path.parent.mkdir(parents=True, exist_ok=True)
    timing_path.write_text(
        json.dumps(
            {
                "elapsed_s": float(dt_all),
                "step_elapsed_s": {k: float(v) for k, v in sorted(step_times.items())},
                "generated_at_unix_s": time.time(),
                "python": sys.executable,
            },
            indent=2,
            sort_keys=True,
        )
        + "\n",
        encoding="utf-8",
    )


if __name__ == "__main__":
    main()
