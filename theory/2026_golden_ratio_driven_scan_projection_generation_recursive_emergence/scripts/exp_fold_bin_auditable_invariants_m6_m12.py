#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Auditable bin-fold invariants from degeneracy spectra (m=6,8,10,12).

All code is English-only by repository convention.

Inputs (generated by the existing pipeline):
  - artifacts/export/fold_bin_degeneracy_spectrum_m6_m12.json

We compute, for each m:
  - Col^{bin}_m = (1/2^{2m}) * sum_{w in X_m} d_m(w)^2
  - g_col^{2,bin}(m) = Col^{bin}_m - 1/|X_m|
  - chi^2(p||u) = |X_m| * Col^{bin}_m - 1 = |X_m| * g_col^{2,bin}(m)
  - log10 |G^{bin}_m| where |G^{bin}_m| = prod_{w in X_m} (d_m(w)!)

Artifacts:
  - artifacts/export/fold_bin_auditable_invariants_m6_m12.json
  - sections/generated/tab_fold_bin_auditable_invariants_m6_m12.tex
"""

from __future__ import annotations

import json
import math
import time
from dataclasses import dataclass
from fractions import Fraction
from pathlib import Path
from typing import Dict, List, Tuple

from common_paths import export_dir, generated_dir
from common_phi_fold import Progress


def _frac(num: int, den: int) -> Fraction:
    return Fraction(int(num), int(den))


def _tex_frac(x: Fraction) -> str:
    if x.denominator == 1:
        return str(x.numerator)
    return f"\\frac{{{x.numerator}}}{{{x.denominator}}}"


def _log10_gauge_order_from_hist(hist: Dict[int, int]) -> float:
    """Compute log10 prod_d (d!)^{count_d} from histogram d -> count."""
    lnG = 0.0
    for d, count in hist.items():
        lnG += float(count) * float(math.lgamma(int(d) + 1))
    return lnG / math.log(10.0)


@dataclass(frozen=True)
class Row:
    m: int
    X_size: int
    col: Fraction
    g_col: Fraction
    chi2: Fraction
    log10_G: float


def _load_deg_spectrum(path: Path) -> List[Dict[str, object]]:
    if not path.is_file():
        raise FileNotFoundError(
            f"Missing input JSON: {path}. Run exp_fold_bin_degeneracy_spectrum_m6_m12.py first."
        )
    payload = json.loads(path.read_text(encoding="utf-8"))
    rows = payload.get("rows")
    if not isinstance(rows, list) or not rows:
        raise ValueError(f"Invalid payload format: expected non-empty 'rows' in {path}")
    return rows


def _compute_rows(rows_in: List[Dict[str, object]], prog: Progress) -> List[Row]:
    out: List[Row] = []
    for i, r in enumerate(rows_in, start=1):
        m = int(r["m"])
        X_size = int(r["X_size"])
        hist_raw = r["hist"]
        if not isinstance(hist_raw, dict):
            raise ValueError(f"Row m={m} has invalid 'hist' type: {type(hist_raw)}")
        hist: Dict[int, int] = {int(k): int(v) for k, v in hist_raw.items()}

        prog.tick(f"compute invariants m={m} ({i}/{len(rows_in)})")

        sum_d2 = sum(int(count) * int(d) * int(d) for d, count in hist.items())
        col = _frac(sum_d2, 1 << (2 * m))
        g_col = col - _frac(1, X_size)
        chi2 = _frac(X_size, 1) * col - _frac(1, 1)
        log10_G = _log10_gauge_order_from_hist(hist)

        out.append(
            Row(
                m=m,
                X_size=X_size,
                col=col,
                g_col=g_col,
                chi2=chi2,
                log10_G=log10_G,
            )
        )
    return out


def _write_tex_table(rows: List[Row], out_path: Path) -> None:
    lines: List[str] = []
    lines.append("% AUTO-GENERATED by scripts/exp_fold_bin_auditable_invariants_m6_m12.py")
    lines.append("\\begin{table}[H]")
    lines.append("\\centering")
    lines.append("\\scriptsize")
    lines.append("\\setlength{\\tabcolsep}{6pt}")
    lines.append(
        "\\caption{二进制区间折叠 $\\mathrm{Fold}^{\\mathrm{bin}}_m$ 的可审计不变量（$m\\in\\{6,8,10,12\\}$）："
        "碰撞概率 $\\mathsf{Col}^{\\mathrm{bin}}_m$、均匀性缺口 $g_{\\mathrm{col}}^{2,\\mathrm{bin}}(m)$、"
        "$\\chi^2$ 散度 $\\chi^2(p^{\\mathrm{bin}}_m\\|u_m)$，以及 gauge 群体积的十进对数 "
        "$\\log_{10}|\\mathcal{G}^{\\mathrm{bin}}_m|$。各项均由表~\\ref{tab:fold_bin_degeneracy_spectrum_m6_m12} "
        "的退化谱直方图直接计算。}"
    )
    lines.append("\\label{tab:fold_bin_auditable_invariants_m6_m12}")
    lines.append("\\begin{tabular}{r r l l l r}")
    lines.append("\\toprule")
    lines.append(
        "$m$ & $|X_m|$ & $\\mathsf{Col}^{\\mathrm{bin}}_m$ & $g_{\\mathrm{col}}^{2,\\mathrm{bin}}(m)$ & "
        "$\\chi^2(p^{\\mathrm{bin}}_m\\|u_m)$ & $\\log_{10}|\\mathcal{G}^{\\mathrm{bin}}_m|$\\\\"
    )
    lines.append("\\midrule")
    for r in rows:
        lines.append(
            f"{r.m} & {r.X_size} & ${_tex_frac(r.col)}$ & ${_tex_frac(r.g_col)}$ & ${_tex_frac(r.chi2)}$ & {r.log10_G:.4f}\\\\"
        )
    lines.append("\\bottomrule")
    lines.append("\\end{tabular}")
    lines.append("\\end{table}")
    lines.append("")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines), encoding="utf-8")


def _write_json(rows: List[Row], out_path: Path, *, wall_s: float, source: str) -> None:
    payload = {
        "version": 1,
        "source": source,
        "wall_time_seconds": float(wall_s),
        "rows": [
            {
                "m": int(r.m),
                "X_size": int(r.X_size),
                "col_bin": {"num": int(r.col.numerator), "den": int(r.col.denominator)},
                "g_col_bin": {"num": int(r.g_col.numerator), "den": int(r.g_col.denominator)},
                "chi2": {"num": int(r.chi2.numerator), "den": int(r.chi2.denominator)},
                "log10_G": float(r.log10_G),
            }
            for r in rows
        ],
    }
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def main() -> None:
    t0 = time.time()
    prog = Progress("fold_bin_audit")

    deg_path = export_dir() / "fold_bin_degeneracy_spectrum_m6_m12.json"
    rows_in = _load_deg_spectrum(deg_path)
    rows = _compute_rows(rows_in, prog)

    wall_s = float(time.time() - t0)

    jout = export_dir() / "fold_bin_auditable_invariants_m6_m12.json"
    _write_json(rows, jout, wall_s=wall_s, source=str(deg_path.name))

    tex_out = generated_dir() / "tab_fold_bin_auditable_invariants_m6_m12.tex"
    _write_tex_table(rows, tex_out)

    print(f"[fold_bin_audit] wrote {jout}", flush=True)
    print(f"[fold_bin_audit] wrote {tex_out}", flush=True)
    print(f"[fold_bin_audit] done wall_s={wall_s:.3f}", flush=True)


if __name__ == "__main__":
    main()

