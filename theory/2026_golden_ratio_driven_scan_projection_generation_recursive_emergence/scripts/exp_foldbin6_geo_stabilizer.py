#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Geometric stabilizer of Fold^{bin}_6 inside Aut(Q6).

All code is English-only by repository convention.

We view Omega_6={0,1}^6 with the hypercube graph Q_6. Its full graph automorphism group is
  Aut(Q6) ≅ (Z2)^6 ⋊ S6,
generated by coordinate permutations and coordinate bit-flips (complements).

Define the label map
  F6(omega) := Fold^{bin}_6(int_6(omega)) ∈ X6,
where Fold^{bin}_6 is the Zeckendorf-prefix fold on the binary interval {0,...,63}.

We compute the geometric stabilizer
  Stab_geo(F6) := { sigma ∈ Aut(Q6) : F6 ∘ sigma = F6 }.

Outputs:
  - artifacts/export/foldbin6_geo_stabilizer.json
  - sections/generated/eq_foldbin6_geo_stabilizer.tex
"""

from __future__ import annotations

import argparse
import itertools
import json
from pathlib import Path
from typing import Dict, List, Sequence, Tuple

from common_paths import export_dir, generated_dir
from common_phi_fold import word_to_str, zeckendorf_digits


Bits6 = Tuple[int, int, int, int, int, int]
Perm6 = Tuple[int, int, int, int, int, int]


def _bits6(n: int) -> Bits6:
    """Return (b1..b6) for the 6-bit binary expansion of n in [0,63]."""
    if n < 0 or n >= 64:
        raise ValueError("n must be in [0,63]")
    return (
        (n >> 5) & 1,
        (n >> 4) & 1,
        (n >> 3) & 1,
        (n >> 2) & 1,
        (n >> 1) & 1,
        (n >> 0) & 1,
    )


def _int6(bits: Bits6) -> int:
    """Binary index int_6(omega1..omega6)=sum_{k=1}^6 omega_k 2^{6-k}."""
    b1, b2, b3, b4, b5, b6 = bits
    return (b1 << 5) | (b2 << 4) | (b3 << 3) | (b4 << 2) | (b5 << 1) | (b6 << 0)


def _foldbin6_label(n: int) -> str:
    """Fold^{bin}_6(n): Zeckendorf digits prefix of length 6 for n ∈ [0,63]."""
    # For m=6, K(6)=9 (F_{10}=55 ≤ 63 < F_{11}=89), so digits up to k=9 are exact.
    digits = zeckendorf_digits(n, 9)  # digits for weights F_{k+1}, k=1..9
    return word_to_str(digits[:6])


def _apply_aut(n: int, *, perm: Perm6, mask_bits: Bits6, bits_cache: Sequence[Bits6]) -> int:
    """Apply (mask,perm) ∈ (Z2)^6 ⋊ S6 to a 6-bit vertex encoded by n."""
    b = bits_cache[n]
    out = (
        b[perm[0]] ^ mask_bits[0],
        b[perm[1]] ^ mask_bits[1],
        b[perm[2]] ^ mask_bits[2],
        b[perm[3]] ^ mask_bits[3],
        b[perm[4]] ^ mask_bits[4],
        b[perm[5]] ^ mask_bits[5],
    )
    return _int6(out)  # in [0,63]


def _sigma_formula_latex(*, perm: Perm6, mask_bits: Bits6) -> str:
    """Return LaTeX for sigma(omega_1,...,omega_6)= ( ... ) under our conventions."""
    parts: List[str] = []
    for i in range(6):
        src = perm[i] + 1  # 1-indexed in paper
        if mask_bits[i] == 0:
            parts.append(f"\\omega_{src}")
        else:
            parts.append(f"1-\\omega_{src}")
    inner = ",\\ ".join(parts)
    return f"\\bigl({inner}\\bigr)"


def _write_eq_tex(*, stab_size: int, generator: Tuple[Perm6, int] | None, out_path: Path) -> None:
    lines: List[str] = []
    lines.append("% AUTO-GENERATED by scripts/exp_foldbin6_geo_stabilizer.py")

    if generator is None:
        lines.append("\\[")
        lines.append("\\begin{aligned}")
        lines.append(f"\\bigl|\\mathrm{{Stab}}_{{\\mathrm{{geo}}}}(F_6)\\bigr|&={stab_size}.")
        lines.append("\\end{aligned}")
        lines.append("\\]")
        lines.append("")
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text("\n".join(lines), encoding="utf-8")
        return

    perm, mask_int = generator
    mask_bits = _bits6(mask_int)
    sigma_tex = _sigma_formula_latex(perm=perm, mask_bits=mask_bits)

    lines.append("\\[")
    lines.append("\\begin{aligned}")
    lines.append(
        f"\\mathrm{{Stab}}_{{\\mathrm{{geo}}}}(F_6)&=\\{{\\mathrm{{id}},\\sigma_{{\\mathrm{{geo}}}}\\}}\\cong\\ZZ_2,"
        f"\\qquad \\bigl|\\mathrm{{Stab}}_{{\\mathrm{{geo}}}}(F_6)\\bigr|={stab_size},\\\\"
    )
    lines.append(
        "\\sigma_{\\mathrm{geo}}(\\omega_1,\\omega_2,\\omega_3,\\omega_4,\\omega_5,\\omega_6)"
        f"&={sigma_tex}."
    )
    lines.append("\\end{aligned}")
    lines.append("\\]")
    lines.append("")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    ap = argparse.ArgumentParser(description="Enumerate the geometric stabilizer of Fold^{bin}_6 in Aut(Q6).")
    ap.add_argument(
        "--json-out",
        type=str,
        default=str(export_dir() / "foldbin6_geo_stabilizer.json"),
        help="Path to JSON audit output.",
    )
    ap.add_argument(
        "--tex-eq-out",
        type=str,
        default=str(generated_dir() / "eq_foldbin6_geo_stabilizer.tex"),
        help="Path to generated TeX equation snippet (\\input{}).",
    )
    args = ap.parse_args()

    bits_cache: List[Bits6] = [_bits6(n) for n in range(64)]
    label: List[str] = [_foldbin6_label(n) for n in range(64)]

    aut_size = 64 * 720
    id_perm: Perm6 = (0, 1, 2, 3, 4, 5)

    stabilizer: List[Tuple[Perm6, int]] = []
    for perm in itertools.permutations(range(6)):
        perm6 = tuple(int(x) for x in perm)  # type: ignore[assignment]
        for mask_int in range(64):
            mask_bits = bits_cache[mask_int]
            ok = True
            for n in range(64):
                n2 = _apply_aut(n, perm=perm6, mask_bits=mask_bits, bits_cache=bits_cache)
                if label[n2] != label[n]:
                    ok = False
                    break
            if ok:
                stabilizer.append((perm6, int(mask_int)))

    # Canonical sort for audit stability.
    stabilizer.sort(key=lambda pm: (pm[1], pm[0]))
    stab_size = len(stabilizer)

    # Identify generator when stab_size==2 (id + unique nontrivial element).
    generator: Tuple[Perm6, int] | None = None
    if stab_size == 2:
        nontrivial = [pm for pm in stabilizer if not (pm[0] == id_perm and pm[1] == 0)]
        generator = nontrivial[0] if nontrivial else None

    payload: Dict[str, object] = {
        "m": 6,
        "K_of_m": 9,
        "aut_size_expected": aut_size,
        "aut_representation": {
            "convention": "new[i] = old[perm[i]] XOR mask_bits[i] for i=0..5",
            "perm_is_0_indexed": True,
            "mask_bits_order": "mask_bits=(b1..b6) corresponds to weights (2^5..2^0)",
        },
        "stabilizer_size": stab_size,
        "stabilizer_elements": [
            {
                "perm_0_indexed": list(perm),
                "perm_1_indexed": [p + 1 for p in perm],
                "mask_int": int(mask_int),
                "mask_bits": "".join(str(b) for b in _bits6(int(mask_int))),
            }
            for perm, mask_int in stabilizer
        ],
    }

    if generator is not None:
        perm, mask_int = generator
        mask_bits = _bits6(mask_int)
        sigma_tex = _sigma_formula_latex(perm=perm, mask_bits=mask_bits)

        # Support / shift diagnostics on integer indices.
        moved: List[int] = []
        fixed: List[int] = []
        delta_hist: Dict[int, int] = {}
        cond_hist: Dict[str, int] = {"omega1_eq_omega5": 0, "omega1_neq_omega5": 0}
        cond_moved_hist: Dict[str, int] = {"omega1_eq_omega5": 0, "omega1_neq_omega5": 0}
        for n in range(64):
            n2 = _apply_aut(n, perm=perm, mask_bits=mask_bits, bits_cache=bits_cache)
            if n2 == n:
                fixed.append(n)
            else:
                moved.append(n)
            d = int(n2) - int(n)
            delta_hist[d] = delta_hist.get(d, 0) + 1

            b = bits_cache[n]
            key = "omega1_eq_omega5" if (b[0] == b[4]) else "omega1_neq_omega5"
            cond_hist[key] += 1
            if n2 != n:
                cond_moved_hist[key] += 1

        payload["generator"] = {
            "perm_0_indexed": list(perm),
            "perm_1_indexed": [p + 1 for p in perm],
            "mask_int": int(mask_int),
            "mask_bits": "".join(str(b) for b in mask_bits),
            "sigma_latex": sigma_tex,
            "fixed_count": len(fixed),
            "moved_count": len(moved),
            "delta_histogram": {str(k): int(v) for k, v in sorted(delta_hist.items())},
            "condition_counts": cond_hist,
            "condition_moved_counts": cond_moved_hist,
        }

    # Write outputs.
    json_out = Path(str(args.json_out))
    json_out.parent.mkdir(parents=True, exist_ok=True)
    json_out.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")

    tex_out = Path(str(args.tex_eq_out))
    _write_eq_tex(stab_size=stab_size, generator=generator, out_path=tex_out)

    print(f"[foldbin6_geo_stab] wrote {json_out}", flush=True)
    print(f"[foldbin6_geo_stab] wrote {tex_out}", flush=True)


if __name__ == "__main__":
    main()

