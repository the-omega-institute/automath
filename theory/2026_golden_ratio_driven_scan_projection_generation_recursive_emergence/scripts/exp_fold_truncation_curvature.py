#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Exact enumeration of the truncation-vs-fold curvature K_{m+1->m}.

This script is English-only by repository convention.

We compare two stable outputs in X_m for a length-(m+1) micro word omega:
  (A) fold-after-truncate:   Fold_m( tau_{m+1->m}(omega) )
  (B) truncate-after-fold:   pi_{m+1->m}( Fold_{m+1}(omega) )

Define:
  H_m(omega) := Hamming distance between (A) and (B) (both length m, golden-legal)
  K_{m+1->m}(omega) := 1[ H_m(omega) > 0 ]

Outputs:
- artifacts/export/fold_truncation_curvature_stats.json
- sections/generated/tab_fold_truncation_curvature_stats.tex
"""

from __future__ import annotations

import json
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List

from common_paths import export_dir, paper_root
from common_fold_map_cache import fold_map_packed
from common_phi_fold import Progress


ROOT = paper_root()
GEN = ROOT / "sections" / "generated"


def build_fold_map(m: int, prog: Progress) -> object:
    """Map w in [0,2^m) to packed Fold_m(word(w)) in [0,2^m)."""
    return fold_map_packed(m, prog=prog)


@dataclass
class Row:
    m: int
    total: int
    p_curv: float
    mean_h: float
    mean_h_over_m: float
    max_h: int


def write_tex(rows: List[Row], out_path: Path) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    lines: List[str] = []
    lines.append("% Auto-generated by scripts/exp_fold_truncation_curvature.py")
    lines.append("\\begin{table}[H]")
    lines.append("\\centering")
    lines.append("\\small")
    lines.append("\\setlength{\\tabcolsep}{8pt}")
    lines.append("\\renewcommand{\\arraystretch}{1.12}")
    lines.append("\\begin{tabular}{rrrrrr}")
    lines.append("\\toprule")
    lines.append(
        "$m$ & $|\\Omega_{m+1}|$ & $\\mathbb P[\\mathcal K_{m+1\\to m}=1]$ & $\\mathbb E[H_m]$ & $\\mathbb E[H_m]/m$ & $\\max H_m$ \\\\"
    )
    lines.append("\\midrule")
    for r in rows:
        lines.append(
            f"{r.m} & {r.total} & {r.p_curv:.6g} & {r.mean_h:.6g} & {r.mean_h_over_m:.6g} & {r.max_h} \\\\"
        )
    lines.append("\\bottomrule")
    lines.append("\\end{tabular}")
    lines.append(
        "\\caption{Exact enumeration under the uniform baseline $\\omega\\sim\\mathrm{Unif}(\\Omega_{m+1})$ of the truncation--fold curvature indicator $\\mathcal K_{m+1\\to m}(\\omega)=\\mathbf 1[H_m(\\omega)>0]$, where $H_m(\\omega)$ is the Hamming distance between the two stable outputs $\\Fold_m(\\tau_{m+1\\to m}(\\omega))$ and $\\pi_{m+1\\to m}(\\Fold_{m+1}(\\omega))$.}"
    )
    lines.append("\\label{tab:fold_truncation_curvature_stats}")
    lines.append("\\end{table}")
    lines.append("")
    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    t0 = time.time()
    prog = Progress("fold_truncation_curvature", every_seconds=20.0)

    m_max = 20

    # Prebuild fold maps for lengths 1..(m_max+1).
    fold_maps: Dict[int, object] = {}
    for L in range(1, m_max + 2):
        fold_maps[L] = build_fold_map(L, prog)

    rows: List[Row] = []
    audit: Dict[str, Dict[str, str]] = {}

    for m in range(1, m_max + 1):
        L = m + 1
        size = 1 << L
        fold_map_m = fold_maps[m]
        fold_map_L = fold_maps[L]

        count_curv = 0
        total_h = 0
        max_h = -1
        argmax_w = 0

        for w in range(size):
            prefix_m = w >> 1  # tau_{m+1->m}(omega) packed as m bits
            a = int(fold_map_m[prefix_m])  # Fold_m(tau(omega)) packed
            folded_L = int(fold_map_L[w])  # Fold_{m+1}(omega) packed length L
            b = folded_L >> 1  # pi_{m+1->m}(Fold_{m+1}(omega)) packed
            h = int(a ^ b).bit_count()
            total_h += h
            if h > 0:
                count_curv += 1
            if h > max_h:
                max_h = h
                argmax_w = w
            prog.tick(f"m={m} w={w}/{size} max_h={max_h} p~={count_curv/float(w+1):.6g}")

        p_curv = count_curv / float(size)
        mean_h = total_h / float(size)
        mean_h_over_m = mean_h / float(m)

        rows.append(
            Row(
                m=m,
                total=size,
                p_curv=p_curv,
                mean_h=mean_h,
                mean_h_over_m=mean_h_over_m,
                max_h=max_h,
            )
        )

        # Minimal audit snippet for the worst-case witness at this m.
        def _bits_str(x: int, n: int) -> str:
            return "".join("1" if ((x >> (n - 1 - i)) & 1) else "0" for i in range(n))

        prefix_m = argmax_w >> 1
        a = int(fold_map_m[prefix_m])
        b = int(fold_map_L[argmax_w]) >> 1
        audit[str(m)] = {
            "argmax_omega": _bits_str(argmax_w, L),
            "Fold_m(tau)_packed": str(a),
            "Fold_m(tau)_bits": _bits_str(a, m),
            "pi(Fold_{m+1})_packed": str(b),
            "pi(Fold_{m+1})_bits": _bits_str(b, m),
            "max_Hm": str(max_h),
        }

        print(
            f"[fold_truncation_curvature] done m={m} P[K=1]={p_curv:.6g} E[H]={mean_h:.6g} maxH={max_h}",
            flush=True,
        )

    export_path = export_dir() / "fold_truncation_curvature_stats.json"
    export_path.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "m_max": m_max,
        "rows": [r.__dict__ for r in rows],
        "audit": audit,
        "generated_at_unix_s": time.time(),
    }
    export_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(f"[fold_truncation_curvature] wrote {export_path}", flush=True)

    tex_path = GEN / "tab_fold_truncation_curvature_stats.tex"
    write_tex(rows, tex_path)
    print(f"[fold_truncation_curvature] wrote {tex_path}", flush=True)


if __name__ == "__main__":
    main()

