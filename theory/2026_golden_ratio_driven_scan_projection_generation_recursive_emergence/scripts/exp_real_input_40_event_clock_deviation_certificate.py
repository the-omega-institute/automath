def _fmt(x: float) -> str:
    if math.isnan(x):
        return "\\mathrm{NaN}"
    if not math.isfinite(x):
        return "\\infty"
    return f"{x:.12f}"


def _edge_e(
    states: List[Tuple[str, int, int]],
    kernel_map: Dict[Tuple[str, int], Tuple[str, int]],
    i: int,
    j: int,
) -> int:
    (s, _px, _py) = states[i]
    (dst_state, x, y) = states[j]
    d = int(x) + int(y)
    dst2, e = kernel_map[(s, d)]
    if dst2 != dst_state:
        raise RuntimeError(
            f"edge mismatch: {states[i]} -> {states[j]} gives dst={dst2}"
        )
    return int(e)


def _covariances_event_bit(
    pi: np.ndarray,
    P: np.ndarray,
    states: List[Tuple[str, int, int]],
    kernel_map: Dict[Tuple[str, int], Tuple[str, int]],
    k_max: int,
) -> Tuple[float, List[float]]:
    n = int(P.shape[0])
    eP = np.zeros((n, n), dtype=float)
    g = np.zeros(n, dtype=float)
    for i in range(n):
        for j in range(n):
            pij = float(P[i, j])
            if pij <= 0.0:
                continue
            e = float(_edge_e(states, kernel_map, i=i, j=j))
            eP[i, j] = pij * e
            g[i] += pij * e
    mu = float(np.dot(pi, g))

    covs: List[float] = []
    # k=0: e_0 in {0,1} so E[e_0^2]=E[e_0]=mu.
    cov0 = mu - mu * mu
    covs.append(float(cov0))
    if k_max <= 0:
        return mu, covs

    # For k>=1: E[e_0 e_k] = pi^T * (eP @ (P^{k-1} g)).
    tmp = g.copy()  # P^{0} g
    for k in range(1, k_max + 1):
        ee = float(np.dot(pi, eP @ tmp))
        covs.append(float(ee - mu * mu))
        tmp = P @ tmp
    return mu, covs


def _var_partial_sums_from_cov(
    covs: List[float], n_max: int
) -> Tuple[List[float], List[float]]:
    # covs[k] = Cov(e_0, e_k), k>=0.
    if n_max <= 0:
        return ([], [])
    if len(covs) < n_max:
        raise ValueError("need covs up to n_max-1")

    var_e = float(covs[0])
    vars_t: List[float] = []
    sums: List[float] = []
    s = 0.0
    for n in range(1, n_max + 1):
        v = float(n) * var_e
        # 2 * sum_{k=1}^{n-1} (n-k) Cov(e_0,e_k)
        for k in range(1, n):
            v += 2.0 * float(n - k) * float(covs[k])
        vars_t.append(v)
        s += v
        sums.append(s)
    return vars_t, sums


