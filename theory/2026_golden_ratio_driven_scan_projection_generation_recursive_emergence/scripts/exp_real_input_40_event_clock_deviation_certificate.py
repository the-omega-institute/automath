def _hitting_time_rows(
    *,
    mu: float,
    vars_Tn: List[float],
    delta: float,
    k_list: List[int],
) -> List[Dict[str, float]]:
    # For tau(k) := inf {n : T_n >= k}, we use:
    #   P(tau(k) > n) = P(T_n < k) = P(mu n - T_n > mu n - k)
    #               <= Var(T_n) / (mu n - k)^2,  for mu n > k.
    #   P(tau(k) <= n) = P(T_n >= k) = P(T_n - mu n >= k - mu n)
    #                 <= Var(T_n) / (k - mu n)^2, for k > mu n.
    # (Chebyshev, one-sided via |X| >= a.)
    if not (math.isfinite(mu) and mu > 0.0):
        raise ValueError("mu must be finite and > 0")
    if not (math.isfinite(delta) and delta > 0.0 and delta < 1.0):
        raise ValueError("delta must be in (0,1)")

    n_max = len(vars_Tn)
    rows: List[Dict[str, float]] = []
    for k in k_list:
        kk = float(k)
        k_over_mu = kk / float(mu)

        # Left tail: find the largest n with k > mu n and bound <= delta.
        n_lo = float("nan")
        p_lo = float("nan")
        gap_lo = float("nan")
        for n in range(1, n_max + 1):
            gap = kk - float(mu) * float(n)
            if gap <= 0.0:
                break
            v = float(vars_Tn[n - 1])
            bound = v / (gap * gap) if gap > 0.0 else float("inf")
            if bound <= delta:
                n_lo = float(n)
                p_lo = float(bound)
                gap_lo = float(gap)

        # Right tail: find the smallest n with mu n > k and bound <= delta.
        n_hi = float("nan")
        p_hi = float("nan")
        gap_hi = float("nan")
        for n in range(1, n_max + 1):
            gap = float(mu) * float(n) - kk
            if gap <= 0.0:
                continue
            v = float(vars_Tn[n - 1])
            bound = v / (gap * gap) if gap > 0.0 else float("inf")
            if bound <= delta:
                n_hi = float(n)
                p_hi = float(bound)
                gap_hi = float(gap)
                break

        rows.append(
            {
                "k": float(k),
                "k_over_mu": float(k_over_mu),
                "n_lo": float(n_lo),
                "p_lo": float(p_lo),
                "gap_lo": float(gap_lo),
                "n_hi": float(n_hi),
                "p_hi": float(p_hi),
                "gap_hi": float(gap_hi),
            }
        )
    return rows


