"""Event clock deviation certificate (real-input 40-state kernel).

We treat the unweighted (theta=0) Parry equilibrium as an internal source of randomness.
The event clock is the additive functional

  T_n := sum_{t=0}^{n-1} e_t,

where e_t in {0,1} is the kernel output bit at step t.

This script computes, by finite-dimensional linear algebra:

1) The stationary Parry Markov chain (pi,P) for the 40-state real-input adjacency.
2) The autocovariance Cov(e_0, e_k) for k=0..K.
3) The finite-n variance Var(T_n) for n=1..N, and the partial sums S_n:=sum_{t<=n}Var(T_t).

From (S_n), we produce an explicit, auditable Chebyshev+union certificate:

  P( max_{1<=t<=n} |T_t - mu_e t| >= b_n(delta) ) <= delta,
  b_n(delta) := sqrt(S_n / delta).

Outputs:
- artifacts/export/real_input_40_event_clock_deviation_certificate.json
- sections/generated/tab_real_input_40_event_clock_deviation_certificate.tex
- sections/generated/tab_real_input_40_hitting_time_deviation_certificate.tex
"""

