@dataclass(frozen=True)
class Payload:
    model: str
    delta: float
    mu_event: float
    p2: float
    n_max: int
    cov_k_max: int
    covs: List[float]
    vars_Tn: List[float]
    sums_Sn: List[float]
    rows: List[Dict[str, float]]
    ht_k_list: List[int]
    ht_rows: List[Dict[str, float]]


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Event clock deviation certificate (real-input 40)."
    )
    parser.add_argument("--delta", type=float, default=0.05)
    parser.add_argument("--n-max", type=int, default=200)
    parser.add_argument("--rows", type=str, default="3,10,20,50,100,200")
    parser.add_argument(
        "--cumulants-json",
        type=str,
        default="artifacts/export/real_input_40_output_potential_cumulants_closed.json",
    )
    parser.add_argument(
        "--json-out",
        type=str,
        default=str(
            export_dir() / "real_input_40_event_clock_deviation_certificate.json"
        ),
    )
    parser.add_argument(
        "--tex-out",
        type=str,
        default=str(
            generated_dir() / "tab_real_input_40_event_clock_deviation_certificate.tex"
        ),
    )
    parser.add_argument(
        "--ht-tex-out",
        type=str,
        default=str(
            generated_dir() / "tab_real_input_40_hitting_time_deviation_certificate.tex"
        ),
    )
    parser.add_argument(
        "--ht-k-list",
        type=str,
        default="5,10,20,50",
        help="Comma-separated event thresholds k for tau(k) certificate.",
    )
    args = parser.parse_args()

    delta = float(args.delta)
    n_max = int(args.n_max)
    row_ns = [int(x.strip()) for x in str(args.rows).split(",") if x.strip()]
    row_ns = sorted({x for x in row_ns if x >= 1})
    if not row_ns:
        raise SystemExit("empty --rows")

    cum = json.loads(
        (Path(__file__).resolve().parent.parent / args.cumulants_json).read_text(
            encoding="utf-8"
        )
    )
    mu = float(cum.get("P1_float", float("nan")))
    p2 = float(cum.get("P2_float", float("nan")))

    edges = build_kernel_edges()
    kernel_map = build_kernel_map(edges)
    states = build_real_input_states()
    B0 = build_weighted_matrix(0.0, 0.0, 0.0, states, kernel_map)
    lam, _eigvals, v, u = _pick_pf_eigpair(B0)
    pi, P = parry_markov(B0, lam=lam, v=v, u=u)

    cov_k_max = max(n_max - 1, max(row_ns))
    mu2, covs = _covariances_event_bit(pi, P, states, kernel_map, k_max=cov_k_max)
    if math.isfinite(mu) and math.isfinite(mu2) and abs(mu - mu2) > 5e-10:
        # keep internal consistency for downstream.
        mu = mu2

    vars_Tn, sums_Sn = _var_partial_sums_from_cov(covs, n_max=n_max)

    ht_k_list = [int(x.strip()) for x in str(args.ht_k_list).split(",") if x.strip()]
    ht_k_list = sorted({k for k in ht_k_list if k >= 1})
    ht_rows = _hitting_time_rows(mu=mu, vars_Tn=vars_Tn, delta=delta, k_list=ht_k_list)

    rows: List[Dict[str, float]] = []
    for n in row_ns:
        if n > n_max:
            continue
        var_Tn = float(vars_Tn[n - 1])
        Sn = float(sums_Sn[n - 1])
        b = math.sqrt(Sn / delta) if (delta > 0.0 and Sn >= 0.0) else float("inf")
        abs_gap = (
            abs(float(var_Tn / float(n)) - float(p2))
            if math.isfinite(p2)
            else float("nan")
        )
        rel_gap = (
            (abs_gap / abs(float(p2)))
            if (math.isfinite(abs_gap) and math.isfinite(p2) and float(p2) != 0.0)
            else float("nan")
        )
        rows.append(
            {
                "n": float(n),
                "mu_n": float(mu * n),
                "var_Tn": float(var_Tn),
                "var_Tn_over_n": float(var_Tn / float(n)),
                "Sn": float(Sn),
                "abs_gap": float(abs_gap),
                "rel_gap": float(rel_gap),
                "b_uniform": float(b),
            }
        )

    jout = Path(args.json_out)
    jout.parent.mkdir(parents=True, exist_ok=True)
    payload = Payload(
        model="real_input_40",
        delta=float(delta),
        mu_event=float(mu),
        p2=float(p2),
        n_max=int(n_max),
        cov_k_max=int(cov_k_max),
        covs=[float(x) for x in covs[: cov_k_max + 1]],
        vars_Tn=[float(x) for x in vars_Tn],
        sums_Sn=[float(x) for x in sums_Sn],
        rows=rows,
        ht_k_list=[int(k) for k in ht_k_list],
        ht_rows=ht_rows,
    )
    jout.write_text(
        json.dumps(asdict(payload), indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )
    print(f"[event-clock-cert] wrote {jout}", flush=True)

    tout = Path(args.tex_out)
    _write_tex(tout, delta=delta, mu=mu, p2=p2, rows=rows)
    print(f"[event-clock-cert] wrote {tout}", flush=True)

    ht_out = Path(args.ht_tex_out)
    _write_tex_hitting_time(ht_out, delta=delta, mu=mu, rows=ht_rows)
    print(f"[event-clock-cert] wrote {ht_out}", flush=True)


if __name__ == "__main__":
    main()
